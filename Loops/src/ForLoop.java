
public class ForLoop {

	public static void main(String[] args) {
	//for(initialization; condition; update)
		for(int i=1; i<=10; i++){
			/*
			 * while-- used when number of iterations are unknown
			 * For-- no. of iterations are known!
			 * DoWhile-- runs at least once irrespective of the condition!
			 * 
			 */
			//at least 2 semicolons or it gives an error
			System.out.println(i);
		
		}
	}

}
